package com.nuoshi.console.domain.audit;

public class AuditStep {
	private int stepBase;
	private int stepEstate;
	private int stepLayout;
	private int stepInner;
	public int getStepBase() {
		return stepBase;
	}
	public void setStepBase(int stepBase) {
		this.stepBase = stepBase;
	}
	public int getStepEstate() {
		return stepEstate;
	}
	public void setStepEstate(int stepEstate) {
		this.stepEstate = stepEstate;
	}
	public int getStepLayout() {
		return stepLayout;
	}
	public void setStepLayout(int stepLayout) {
		this.stepLayout = stepLayout;
	}
	public int getStepInner() {
		return stepInner;
	}
	public void setStepInner(int stepInner) {
		this.stepInner = stepInner;
	}
	
	
}
