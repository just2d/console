package com.nuoshi.console.domain.audit;

public class AgentAuditTask {
	private Integer id;
	private Integer agentId;
	private Integer cityId;
	private Integer identityCard;
	private Integer headPhoto;
	private Integer nameCard;
	private Integer auditorId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getAgentId() {
		return agentId;
	}
	public void setAgentId(Integer agentId) {
		this.agentId = agentId;
	}
	public Integer getCityId() {
		return cityId;
	}
	public void setCityId(Integer cityId) {
		this.cityId = cityId;
	}
	public Integer getIdentityCard() {
		return identityCard;
	}
	public void setIdentityCard(Integer identityCard) {
		this.identityCard = identityCard;
	}
	public Integer getHeadPhoto() {
		return headPhoto;
	}
	public void setHeadPhoto(Integer headPhoto) {
		this.headPhoto = headPhoto;
	}
	public Integer getNameCard() {
		return nameCard;
	}
	public void setNameCard(Integer nameCard) {
		this.nameCard = nameCard;
	}
	public Integer getAuditorId() {
		return auditorId;
	}
	public void setAuditorId(Integer auditorId) {
		this.auditorId = auditorId;
	}
	
	
	
	

}
